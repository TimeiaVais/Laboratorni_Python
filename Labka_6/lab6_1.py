#Знайти найбільший елемент серед елементів вектора x є R**n з парними індексами

a = [1, 5, 10, 8, 15, 22, 333, 7007, 54, 72,61]
max_num = a[0]
for i in range(2,len(a),2):
    if max_num < a[i]:
        max_num = a[i]
print('Найбільший елемент : {0} '.format(max_num))
